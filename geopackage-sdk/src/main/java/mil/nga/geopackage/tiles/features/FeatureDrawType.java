package mil.nga.geopackage.tiles.features;

/**
 * Feature Draw Types when drawing feature tiles
 *
 * @author osbornb
 * @since 3.2.0
 */
public enum FeatureDrawType {

    /**
     * Circle for a point
     */
    CIRCLE,

    /**
     * Stroke for a line or polygon
     */
    STROKE,

    /**
     * Fill for a polygon
     */
    FILL

}
